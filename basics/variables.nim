# Declaration
var name: string
name = "Jota"
echo name

# Also works
var name2 = "Jota"
echo name2

# let keyword doesn't allow reassignment
let name3: string = "Jota"
# `name3 = "Samuel"` CAUSES AN ERROR]
echo name3

# const keyword is similar to let, but it needs to assign a value when declarating it
const name4 = "Jota"
echo name4